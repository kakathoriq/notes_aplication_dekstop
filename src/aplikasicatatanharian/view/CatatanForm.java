    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String judul = txtJudul.getText();
        String deskripsi = txtDeskripsi.getText();

        if (judul.isEmpty() || deskripsi.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field wajib diisi.");
            return;
        }

        Catatan c = new Catatan();
        c.setJudul(judul);
        c.setDeskripsi(deskripsi);

        if (btnSimpan.getText().equals("Simpan")) {
            dao.insert(c);
            JOptionPane.showMessageDialog(this, "Data berhasil disimpan.");
        } else {
            c.setId(idTerpilih);
            dao.update(c);
            JOptionPane.showMessageDialog(this, "Data berhasil diperbarui.");
        }

        tampilData();
        resetForm();
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void JtabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JtabelMouseClicked
         if (evt.getClickCount() == 2) {
            int baris = Jtabel.getSelectedRow();
            if (baris != -1) {
                idTerpilih = (int) Jtabel.getValueAt(baris, 0);
                String judul = Jtabel.getValueAt(baris, 1).toString();
                String deskripsi = Jtabel.getValueAt(baris, 2).toString();

                String[] opsi = {"Edit", "Hapus", "Batal"};
                int pilihan = JOptionPane.showOptionDialog(this, "Pilih aksi:", "Aksi Data",
                        JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE,
                        null, opsi, opsi[0]);

                if (pilihan == 0) {
                    txtJudul.setText(judul);
                    txtDeskripsi.setText(deskripsi);
                    btnSimpan.setText("Update");
                } else if (pilihan == 1) {
                    dao.delete(idTerpilih);
                    tampilData();
                    JOptionPane.showMessageDialog(this, "Data berhasil dihapus.");
                }
            }
        }
    }//GEN-LAST:event_JtabelMouseClicked

