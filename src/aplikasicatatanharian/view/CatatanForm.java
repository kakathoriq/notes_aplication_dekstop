    private void tampilData() {
        DefaultTableModel model = new DefaultTableModel(new String[]{"ID", "Judul", "Deskripsi"}, 0);
        List<Catatan> list = dao.getAll();

        for (Catatan c : list) {
            model.addRow(new Object[]{c.getId(), c.getJudul(), c.getDeskripsi()});
        }

        Jtabel.setModel(model);
        Jtabel.setDefaultEditor(Object.class, null);
    }

